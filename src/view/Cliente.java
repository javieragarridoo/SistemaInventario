    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtId.getText().isEmpty()) {
            int confirmar = JOptionPane.showConfirmDialog(null, "¬øDesea eliminar el cliente?", "Advertencia", 2);
            if(confirmar == 0){
                clientes.setId_cliente(Integer.parseInt(txtId.getText()));
                daoCliente.eliminarCliente(clientes);
                limpiarClientes();
                mostrarClientes();
                limpiarCampos();
                JOptionPane.showMessageDialog(null, "Cliente eliminado correctamente");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        clientes.setId_cliente(Integer.parseInt(txtId.getText()));
        if(daoCliente.buscarClientePorId(clientes)){
            txtId.setText(clientes.getId_cliente()+ "");
            txtNombre.setText(clientes.getNombre());
            txtApellido.setText(clientes.getApellido());
            txtDocumento.setText(clientes.getDocumento());
            txtDireccion.setText(clientes.getDireccion());
            txtTelefono.setText(clientes.getTelefono());
            txtCorreo.setText(clientes.getCorreo());
        }else{
            JOptionPane.showMessageDialog(null, "El cliente no existe");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    void limpiarCampos(){
        txtId.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtDocumento.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtCorreo.setText("");
    }
    
    void limpiarClientes(){
        for (int i = 0; i < modeloCliente.getRowCount(); i++) {
            modeloCliente.removeRow(i);
            i = 0 - 1;
        }
    }
    
