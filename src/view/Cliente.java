    private void mostrarClientes(){
        List<Clientes> listaClientes = daoCliente.listarClientes();
        modeloCliente = (DefaultTableModel) tblClientes.getModel();
        Object[] objeto = new Object[7];
        
        for(int i = 0; i < listaClientes.size(); i++){
            objeto[0] = listaClientes.get(i).getId_cliente();
            objeto[1] = listaClientes.get(i).getNombre();
            objeto[2] = listaClientes.get(i).getApellido();
            objeto[3] = listaClientes.get(i).getDocumento();
            objeto[4] = listaClientes.get(i).getDireccion();
            objeto[5] = listaClientes.get(i).getTelefono();
            objeto[6] = listaClientes.get(i).getCorreo();
            
            modeloCliente.addRow(objeto);
        }
        tblClientes.setModel(modeloCliente);
    }

    
